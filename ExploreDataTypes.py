# Creating variables of different data types
integer_value = 42           # int
float_value = 3.14           # float
string_value = "Hello"       # str
boolean_value = True         # bool

# Printing the values and their data types
print("Value:", integer_value, "Type:", type(integer_value))
print("Value:", float_value, "Type:", type(float_value))
print("Value:", string_value, "Type:", type(string_value))
print("Value:", boolean_value, "Type:", type(boolean_value))
